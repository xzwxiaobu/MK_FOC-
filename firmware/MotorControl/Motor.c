/* 建立反正切表加快计算速度 */
float my_atan2(float x, float y)
{
    unsigned short idx;
    float k = y / x;
    float atan_val;

    if(k>1)
    {
        idx = 512*(k-1)/(k+1);
        atan_val = atanTab[idx] + 0.785398f;
    }
    else if(k>=0&&k<=1)
    {
        idx = 512*k;
        atan_val = atanTab[idx];
    }
    else if(k>=-1&&k<0)
    {
        idx = 512*(-k);
        atan_val = -atanTab[idx];
    }
    else
    {
        idx = 512*(-k-1)/(-k+1);
        atan_val = -atanTab[idx] - 0.785398f;
    }

    if(x<0) // 第二, 第三象限
    {
        return atan_val + 3.141593f;
    }
    else
    {
        if(y>0) // 第一象限
            return atan_val;
        else // 第四象限
            return atan_val + 6.283185f;
    }
}

//快速反正切计算
float my_atan(float x)
{
    unsigned short idx;
    float atan_val;

    if (x > 1)
    {
        idx = 512 * (x - 1) / (x + 1);
        atan_val = atanTab[idx] + 0.785398f;
    }
    else if (x >= 0 && x <= 1)
    {
        idx = 512 * x;
        atan_val = atanTab[idx];
    }
    else if (x >= -1 && x < 0)
    {
        idx = 512 * (-x);
        atan_val = -atanTab[idx];
    }
    else
    {
        idx = 512 * (-x - 1) / (-x + 1);
        atan_val = -atanTab[idx] - 0.785398f;
    }

    return atan_val;
}

